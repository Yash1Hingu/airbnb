import axios from "axios";
import { useEffect, useState } from "react";
import { useParams } from "react-router-dom";
import Loader from "../PageUI/Loader";
import PhoosView from "../PageComponents/PhoosView";
import { differenceInCalendarDays, format } from "date-fns";
import { dateShow } from "../../util/dateShow";
import ruppesShow from "../../util/ruppesShow";

export default function BookingPage() {
    const { id } = useParams();
    const [bookPlace, setBookPlace] = useState({});
    const [isLoading, setIsLoading] = useState(true);
    useEffect(() => {
        axios.get('/booking/' + id).then((res) => {
            setBookPlace(res.data.place);
            setIsLoading(false);
        })
    }, [])
    return (<>

        {isLoading &&
            <div className="mb-4 mx-auto text-center mt-12">
                <Loader />
            </div>
        }
        {!isLoading &&
            <div className=" -mx-4 py-4 px-8 my-8 rounded-2xl">
                <h1 className="text-2xl mb-4 font-bold">{bookPlace.title}</h1>
                <a
                    href={"https://maps.google.com/?q=" + bookPlace.address}
                    className="text-xl underline hover:text-blue-600 flex gap-1 items-center"
                >
                    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-6 h-6">
                        <path fillRule="evenodd" d="m11.54 22.351.07.04.028.016a.76.76 0 0 0 .723 0l.028-.015.071-.041a16.975 16.975 0 0 0 1.144-.742 19.58 19.58 0 0 0 2.683-2.282c1.944-1.99 3.963-4.98 3.963-8.827a8.25 8.25 0 0 0-16.5 0c0 3.846 2.02 6.837 3.963 8.827a19.58 19.58 0 0 0 2.682 2.282 16.975 16.975 0 0 0 1.145.742ZM12 13.5a3 3 0 1 0 0-6 3 3 0 0 0 0 6Z" clipRule="evenodd" />
                    </svg>
                    {bookPlace.address}
                </a>
                <div>
                    <div className="flex justify-between items-center p-4 bg-gray-200 my-2 rounded-2xl text-black">
                        <div>
                            <h1 className="text-2xl font-bold">Your Booking information:</h1>
                            <div className="flex gap-2 items-center">
                                <h3 className="flex gap-2 mt-3 text-black font-bold">
                                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6">
                                        <path strokeLinecap="round" strokeLinejoin="round" d="M21.752 15.002A9.72 9.72 0 0 1 18 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 0 0 3 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 0 0 9.002-5.998Z" />
                                    </svg>
                                    {(bookPlace.checkIn && bookPlace.checkOut) && differenceInCalendarDays(new Date(bookPlace.checkOut), new Date(bookPlace.checkIn))} night</h3>
                                <h3 className="flex gap-1 text-gray-900 mt-2">
                                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6">
                                        <path strokeLinecap="round" strokeLinejoin="round" d="M6.75 3v2.25M17.25 3v2.25M3 18.75V7.5a2.25 2.25 0 0 1 2.25-2.25h13.5A2.25 2.25 0 0 1 21 7.5v11.25m-18 0A2.25 2.25 0 0 0 5.25 21h13.5A2.25 2.25 0 0 0 21 18.75m-18 0v-7.5A2.25 2.25 0 0 1 5.25 9h13.5A2.25 2.25 0 0 1 21 11.25v7.5m-9-6h.008v.008H12v-.008ZM12 15h.008v.008H12V15Zm0 2.25h.008v.008H12v-.008ZM9.75 15h.008v.008H9.75V15Zm0 2.25h.008v.008H9.75v-.008ZM7.5 15h.008v.008H7.5V15Zm0 2.25h.008v.008H7.5v-.008Zm6.75-4.5h.008v.008h-.008v-.008Zm0 2.25h.008v.008h-.008V15Zm0 2.25h.008v.008h-.008v-.008Zm2.25-4.5h.008v.008H16.5v-.008Zm0 2.25h.008v.008H16.5V15Z" />
                                    </svg>
                                    <span>
                                        {bookPlace.checkIn && `${dateShow(format(bookPlace.checkIn, 'yyyy-MM-dd'))}`}
                                    </span> -
                                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-6 h-6">
                                        <path strokeLinecap="round" strokeLinejoin="round" d="M6.75 3v2.25M17.25 3v2.25M3 18.75V7.5a2.25 2.25 0 0 1 2.25-2.25h13.5A2.25 2.25 0 0 1 21 7.5v11.25m-18 0A2.25 2.25 0 0 0 5.25 21h13.5A2.25 2.25 0 0 0 21 18.75m-18 0v-7.5A2.25 2.25 0 0 1 5.25 9h13.5A2.25 2.25 0 0 1 21 11.25v7.5m-9-6h.008v.008H12v-.008ZM12 15h.008v.008H12V15Zm0 2.25h.008v.008H12v-.008ZM9.75 15h.008v.008H9.75V15Zm0 2.25h.008v.008H9.75v-.008ZM7.5 15h.008v.008H7.5V15Zm0 2.25h.008v.008H7.5v-.008Zm6.75-4.5h.008v.008h-.008v-.008Zm0 2.25h.008v.008h-.008V15Zm0 2.25h.008v.008h-.008v-.008Zm2.25-4.5h.008v.008H16.5v-.008Zm0 2.25h.008v.008H16.5V15Z" />
                                    </svg>
                                    <span>
                                        {bookPlace.checkOut && `${dateShow(format(bookPlace.checkOut, 'yyyy-MM-dd'))}`}
                                    </span>
                                </h3>
                            </div>
                        </div>
                        <div className="bg-primary p-4 text-white font-black text-2xl rounded-xl">
                            Total Price: <br />
                            <span className="flex gap-1 items-center">
                                <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className="w-6 h-6">
                                    <path fillRule="evenodd" d="M12 2.25c-5.385 0-9.75 4.365-9.75 9.75s4.365 9.75 9.75 9.75 9.75-4.365 9.75-9.75S17.385 2.25 12 2.25ZM9 7.5A.75.75 0 0 0 9 9h1.5c.98 0 1.813.626 2.122 1.5H9A.75.75 0 0 0 9 12h3.622a2.251 2.251 0 0 1-2.122 1.5H9a.75.75 0 0 0-.53 1.28l3 3a.75.75 0 1 0 1.06-1.06L10.8 14.988A3.752 3.752 0 0 0 14.175 12H15a.75.75 0 0 0 0-1.5h-.825A3.733 3.733 0 0 0 13.5 9H15a.75.75 0 0 0 0-1.5H9Z" clipRule="evenodd" />
                                </svg>
                                {ruppesShow(bookPlace.price * differenceInCalendarDays(new Date(bookPlace.checkOut), new Date(bookPlace.checkIn)))}
                            </span>
                        </div>
                    </div>
                </div>
                <PhoosView place={bookPlace} />
            </div>
        }
    </>
    );
}